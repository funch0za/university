def parse_labeltxt(path):
    """Парсинг одного .txt файла с предсказаниями YOLO"""
    with open(path) as f:
        lines = f.readlines()

    labels = []
    for line in lines:
        class_id = int(line.split()[0])
        labels.append(sings_rtsd_names[class_id])

    # Особый случай: если два раза 5.19.1, то добавляем 5.19.2
    if labels.count('5.19.1') > 1:
        labels.append('5.19.2')

    labels = list(set(labels))

    # Конвертация в индексы задачи
    result = []
    for sign in labels:
        if sign in sings_input:
            result.append(sings_input.index(sign) + 1)

    return result


