sample = pd.read_csv('sample_solution.csv')

img2id = dict(zip(test_csv['img'], test_csv['id']))

labels_dir = 'yolov5/runs/detect/yolov5m6_signs_test/labels'

if not os.path.exists(labels_dir):
    print(f'❌ Папка с предсказаниями не найдена: {labels_dir}')
    print('Проверьте, что инференс завершился успешно')
    exit(1)

print('Парсинг предсказаний...')
predicted = {}
for label_file in tqdm(os.listdir(labels_dir), desc='labels'):
    if label_file.endswith('.txt'):
        img_name = label_file[:-4] + '.jpg'  # замена .txt на .jpg
        predicted[img_name] = parse_labeltxt(os.path.join(labels_dir, label_file))

print('Формирование solution.csv...')
for img, signs in predicted.items():
    if img not in img2id:
        print(f'⚠️  Изображение {img} не найдено в test.csv, пропускаю')
        continue

    img_id = img2id[img]

    # Дополняем нулями до 8 знаков
    padded = signs[:8] + [0] * (8 - len(signs))

    # Записываем в таблицу
    for i in range(8):
        col = str(i + 1)
        if col not in sample.columns:
            # Создаём колонку, если её нет
            sample[col] = 0
        sample.loc[sample['id'] == img_id, col] = padded[i]

sample.to_csv('solution.csv', index=False)
